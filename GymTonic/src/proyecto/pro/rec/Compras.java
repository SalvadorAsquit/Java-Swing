    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int seleccionado = tabla1.getSelectedRow();

        List <Articulos> listArticulos;

        try {
            ObjectInputStream ficherocargaArticulos = new ObjectInputStream(new FileInputStream("Articulos.dat"));
            listArticulos =(List <Articulos>) ficherocargaArticulos.readObject();
            ficherocargaArticulos.close();
        } catch (Exception ex) {
            listArticulos = new LinkedList();
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        List <Cliente> listCliente;

        try {
            ObjectInputStream ficherocargaCliente = new ObjectInputStream(new FileInputStream("Cliente.dat"));
            listCliente =(List <Cliente>) ficherocargaCliente.readObject();
            ficherocargaCliente.close();
        } catch (Exception ex) {
            listCliente = new LinkedList();
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        int bajada = Integer.parseInt(cantidadbox.getText());
        bajada =  listArticulos.get(seleccionado).getCantidad() - bajada;
        listArticulos.get(seleccionado).setCantidad(bajada);
        double total = listArticulos.get(seleccionado).getPrecio() * bajada;
        listCliente.get(usuarioconectado).aumentoCuota(total);
        this.cuotabox.setText(String.valueOf(listCliente.get(usuarioconectado).getCuota()));

        try {
            ObjectOutputStream fichero = new  ObjectOutputStream(new FileOutputStream("Articulos.dat"));
            fichero.writeObject(listArticulos);
            fichero.close();
        } catch (IOException ex) {
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            ObjectOutputStream fichero = new  ObjectOutputStream(new FileOutputStream("Cliente.dat"));
            fichero.writeObject(listCliente);
            fichero.close();
        } catch (IOException ex) {
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

