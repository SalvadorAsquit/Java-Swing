    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       List <Cliente> listC;
        boolean existe = false;
        try {
            ObjectInputStream ficherocarga = new ObjectInputStream(new FileInputStream("Cliente.dat"));
            listC =(List <Cliente>) ficherocarga.readObject();
            ficherocarga.close();
        } catch (Exception ex) {
            listC = new LinkedList();
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
        String usuario = usu.getText();
        String password = pass.getText();
        String nombre = nom.getText();
        String dni = id.getText();
        Cliente d = new Cliente(nombre,dni,usuario,password);
        
        for (int i = 0; i<listC.size(); i++){
        if (listC.get(i).getUsuario().equals(d.getUsuario())){
        existe = true;
        }
        }
        if (!existe){
        listC.add(d);
        JOptionPane.showMessageDialog(this, "Se ha Creado el Usuario", "Usuario", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Ya Existe El Usuario", "Usuario", JOptionPane.PLAIN_MESSAGE);
        }
        
        
        try {
            ObjectOutputStream ficheroC = new  ObjectOutputStream(new FileOutputStream("Cliente.dat"));
            ficheroC.writeObject(listC);
            ficheroC.close();
        } catch (IOException ex) {
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

