    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nom = nombre.getText();
        String h = hora.getText();
        String pre = precio.getText();
    
        
        List <Clase> listClase;
        
        try {
            ObjectInputStream ficherocargaclase = new ObjectInputStream(new FileInputStream("ClaseOpcional.dat"));
            listClase =(List <Clase>) ficherocargaclase.readObject();
            ficherocargaclase.close();
        } catch (Exception ex) {
            listClase = new LinkedList();
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        Clase d = new Clase(nom,h,pre);
        
        listClase.add(d);
        JOptionPane.showMessageDialog(this, "Clase a単adida A la espera de Aprobacion", "Usuario", JOptionPane.PLAIN_MESSAGE);
        
        
        
        try {
            ObjectOutputStream fichero = new  ObjectOutputStream(new FileOutputStream("ClaseOpcional.dat"));
            fichero.writeObject(listClase);
            fichero.close();
        } catch (IOException ex) {
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
            this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

