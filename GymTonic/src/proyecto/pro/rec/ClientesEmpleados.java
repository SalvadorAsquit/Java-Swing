    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int seleccionado = tabla1.getSelectedRow();

        List <Cliente> listE;

        try {
            ObjectInputStream ficherocargaCliente = new ObjectInputStream(new FileInputStream("Cliente.dat"));
            listE =(List <Cliente>) ficherocargaCliente.readObject();
            ficherocargaCliente.close();
        } catch (Exception ex) {
            listE = new LinkedList();
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }

        int confirmado = JOptionPane.showConfirmDialog(this,  //padre
            "Eliminar Â¿?", //texto del mensaje
            "Ventana de confirmaciÃ³n", //title de la ventana
            JOptionPane.YES_NO_CANCEL_OPTION); //tipo de confirmaciÃ³n

        //YES_NO_OPTION, YES_NO_CANCEL_OPTION, or OK_CANCEL_OPTION

        if (JOptionPane.OK_OPTION == confirmado) {
            listE.remove(seleccionado);
        }

        try {
            ObjectOutputStream fichero = new  ObjectOutputStream(new FileOutputStream("Cliente.dat"));
            fichero.writeObject(listE);
            fichero.close();
        } catch (IOException ex) {
            Logger.getLogger(ProyectoProRec.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

